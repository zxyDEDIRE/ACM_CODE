const int INF=0x3f3f3f3f;
const int maxn=1e6;
struct Edge{
	int to,next,w;
}edge[maxn];
int head[maxn],cnt;
int dep[maxn];
int n,m,s,t;
void init(){
	cnt=1;
	memset(head+1,0,sizeof(int)*t);
}
void add(int from,int to,int w)
{
	edge[++cnt].w=w;
	edge[cnt].to=to;
	edge[cnt].next=head[from];
	head[from]=cnt;
}
bool bfs(int s,int t)
{
	memset(dep+1,0,sizeof(int)*t);
	queue<int>q;
	q.push(s);
	dep[s]=1;
	while(!q.empty())
	{
		int x=q.front();q.pop();
		if(x==t)return true;
		for(int i=head[x];i;i=edge[i].next)
		{
			int y=edge[i].to;
			int w=edge[i].w;
			if(w&&!dep[y])
				dep[y]=dep[x]+1,q.push(y);
		}
	}
	return false;
}
int dfs(int x,int flow,int t)
{
	if(x==t)return flow;
	int out=flow;
	for(int i=head[x];i;i=edge[i].next)
	{
		int y=edge[i].to;
		int w=edge[i].w;
		if(w&&dep[y]==dep[x]+1)
		{
			int tmp=dfs(y,min(out,w),t);
			edge[i].w-=tmp;
			edge[i^1].w+=tmp;
			out-=tmp;
			if(!out)break;
		}
	}
	if(flow==out)dep[x]=0;
	return flow-out;
}
int DINIC(int s,int t)
{
	int ans=0;
	while(bfs(s,t))
		ans+=dfs(s,INF,t);
	return ans;
}
int f(int x,int y){return n+(x-1)*12+y;}
void solve()
{
	while(cin>>n)
	{
		s=n+12*7+1;
		t=n+12*7+2;
		init();
		for(int i=1;i<=n;i++)
		{
			add(s,i,1);
			add(i,s,0);

			cin>>m;
			for(int j=1;j<=m;j++)
			{
				int x,y;
				cin>>x>>y;
				add(i,f(x,y),1);
				add(f(x,y),i,0);
			}
		}
		for(int i=1;i<=7;i++)
			for(int j=1;j<=12;j++){
				add(f(i,j),t,1);
				add(t,f(i,j),0);
			}
		cout<<DINIC(s,t)<<endl;
	}
}
