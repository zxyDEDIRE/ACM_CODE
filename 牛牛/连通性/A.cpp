const int inf=0x3f3f3f3f;
const int INF=1e9+7;
const int maxn=1e6;
int dfn[maxn],low[maxn],indx;
int in[maxn],col[maxn],tot;
vector<int>v[maxn];
bitset<maxn>vis;
stack<int>s;
int n,m;
void tarjan(int x)
{
	dfn[x]=low[x]=++indx;
	s.push(x);
	vis[x]=1;
	for(auto y:v[x])
	{
		if(!dfn[y])
		{
			tarjan(y);
			low[x]=min(low[x],low[y]);
		}
		else if(vis[y])
			low[x]=min(low[x],dfn[y]);
	}
	if(low[x]==dfn[x])
	{
		tot++;
		int y;
		do{
			y=s.top();s.pop();
			col[y]=tot;
			vis[y]=0;
		}while(x!=y);
	}
}
